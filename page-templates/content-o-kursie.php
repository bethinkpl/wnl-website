				<h2 class="wnl-page-title cele-kursu">Jakie są cele kursu Więcej niż LEK?</h2>
				<h3>Lekarski Egzamin Końcowy</h3>
				<p>Pragniemy pomóc Ci osiągnąć pożądany przez Ciebie wynik na LEKu. Jak to robimy? Przede wszystkim tworzymy narzędzia i środowisko, dzięki któremu zaoszczędzisz masę czasu na samodzielnej selekcji materiału, przebijaniu się przez stosy książek, szukaniu odpowiedzi na “głupie” pytania, czy na układaniu planu pracy.</p>
				<p>Ucząc się razem tworzymy wielką bazę wiedzy, z której każdy kolejny rocznik będzie mógł korzystać. Platforma pozwoli Ci nie tylko szybko i sprawnie się komunikować z innymi ale także znajdować zapomniane informacje, rozwiązywać zadania i monitorować Twój indywidualny postęp.</p>

				<h3>Praca zespołowa</h3>
				<p>Wszystkie te narzędzia mają na celu pomóc nam wszystkim w nauczeniu się czegoś, czego na studiach niestety musieliśmy się uczyć sami - pracy zespołowej. Staż podyplomowy zaraz minie i wszyscy znajdziemy się w nowych środowiskach, wśród ludzi, którzy na co dzień tworzą zgrane zespoły.</p>
				<p>Liczymy na to, że rozpoczniesz swoją zawodową karierę w miejscu gdzie praca zespołowa jest nagradzana, a jeśli tak nie będzie, że dzięki doświadczeniom zdobytym na naszym kursie będziesz zachęcać innych do wspólnego wysiłku i zdrowej komunikacji.</p>

				<h3>Growth mindset</h3>
				<p>Nikt nie wie jakie czekają na nas wyzwania w przyszłości. Niezależnie od tego czy będzie to praca zawodowa, budowa domu czy wychowanie dzieci, wierzymy, że skupianie się na postępie a nie na wyniku przynosi zdecydowanie lepsze rezultaty i pozwala z dużo większym komfortem psychicznym nie tylko znosić porażki ale przede wszystkim się z nich uczyć i dalej rozwijać. Zjawisko to w swoich pracach opisuje Carol Dweck, której twórczość i perspektywę będziemy starali się Wam przybliżyć.</p>

				<h3>Efektywna nauka</h3>
				<p>Na kursie dowiesz się całkiem sporo o tym, jak przebiega proces nauki. Zarówno na warsztatach jak i na platformie z początku kursu pojawią się materiały, dzięki którym dowiesz się jak efektywnie się uczyć i dlaczego kurs Więcej niż LEK jest skonstruowany tak, a nie inaczej. Mamy nadzieję, że pozwoli Ci to nie tylko lepiej przygotować się do egzaminu ale także pomoże na dalszych etapach ścieżki zawodowej i w zmaganiach z egzaminami specjalizacyjnymi.</p>

				<h3>Lekarska perspektywa</h3>
				<p>Na warsztatach poświęcimy także czas na wspólne rozważania na temat tego co na nas czeka po egzaminie oraz na temat przyszłości zawodu lekarza w ogóle. Nowe technologie? Restrukturyzacja medycyny? Ewolucja zawodu lekarza? Sposoby na wykorzystanie naszej wiedzy w przyszłości? To tylko kilka z planowanych przez nas tematów.</p>
