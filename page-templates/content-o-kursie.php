				<h2 class="wnl-page-title">Pytania z innej beczki...</h2>
				<h3>Dlaczego akurat edukacja?</h3>
				<p>Zarówno w firmie jak i poza nią cała ekipa projektu Więcej niż LEK tworzy środowisko ludzi uczących się. Naszym planem jest zarazić Was entuzjazmem do szeroko pojętej nauki i to nie tylko medycyny. :)</p>
				<p>Praktykujemy filozofię “Growth Mindset”, stworzoną i opisaną przez Carol Dweck. Pomagamy sobie, uczymy się od siebie, lubimy popełniać błędy i przede wszystkim oceniamy naszą pracę na podstawie postępu jaki poczyniliśmy a nie tego co dokładnie osiągnęliśmy.</p>
				<p>Dlatego też chcemy uczyć się razem z Wami. W naszym biurze znajdziecie czytelnię i bibliotekę, z której śmiało możecie korzystać. Poza tym zawsze macie nas pod ręką gdybyście potrzebowali pomocy, albo zauważyli jakieś błędy w działaniu platformy i chcieli tym samym pomóc nam :)</p>

				<h3>Robicie tylko kursy, czy chodzi o coś więcej?</h3>
				<p>Traktujemy kurs “Więcej niż LEK” jako okazję do szerzenia idei dzielenia się wiedzą, działania na rzecz <strong>powrotu prawdziwego mentoringu w środowisku medycznym</strong> oraz poprawy wizerunku lekarzy w polskim społeczeństwie. Wierzymy, że aby to stało się rzeczywistością, sami musimy stać się tą zmianą.</p>
				<p>Mamy nadzieję, że zarówno w Waszej, jak i naszej zawodowej codzienności, wszyscy będziemy pamiętać, że aby oczekiwać pomocy, sami musimy być zawsze gotowi udzielić jej drugiej osobie.</p>

				<h3>Skąd Wy się właściwie wzięliście?</h3>
				<p>Jesteśmy grupą przyjaciół z Poznania. Projekt jest w całości prowadzony i tworzony przez lekarza (Daro) i człowieka od wszystkiego (Adam), oraz naszych przyjaciół. Jesteśmy w pełni prywatną firmą, która nie posiada żadnych koneksji z przemysłem farmaceutycznym, ani żadnym innym. Będzie tak, dopóki my jesteśmy odpowiedzialny za prowadzenie projektu “Więcej niż LEK”. ;)</p>

