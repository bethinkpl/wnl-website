				<h3>Czy dostęp do platformy zawiera dostęp do bazy pytań do LEKu?</h3>
				<p>Na dzień 1 kwietnia 2017 roku baza pytań do kursu Więcej niż LEK jest nadal na etapie produkcji. Dostęp do platformy będzie umożliwiał rozwiązanie dużej liczby pytań z poprzednich edycji Lekarskiego Egzaminu Końcowego jednak na tym etapie nie będzie możliwości wykupienia dostępu do samych pytań. Baza pytań będzie zwiększana z dnia na dzień w trakcie pierwszej edycji kursu. Mamy nadzieję, że na pewnym etapie będzie ważnym i samodzielnym elementem platformy.</p>

				<h3>Jak wygląda dzień lekcyjny? Czym jest lekcja na kursie Więcej niż LEK?</h3>
				<p>Każdego dnia lekcyjnego na platformie będzie otwierał się dostęp do 1 nowej lekcji. W ramach kursu “Więcej niż LEK” przewidzianych jest 55 dni lekcyjnych. Nowe lekcje będą pojawiać się każdego dnia roboczego. W weekendy, odpoczywamy bądź nadrabiamy zaległości. ;)</p>

				<p>Dzień lekcyjny to:</p>
				<h4>Wstęp</h4>
				<p>We wstępie będziemy dzielić się z Wami naszym doświadczeniem, humorem i entuzjazmem. Każdego dnia postaramy się Was odpowiednio zmotywować do działania :)</p>
				<h4>Powtórka</h4>
				<p>Na kursie będziemy kładli bardzo duży nacisk na powtórki. Jest to czas i miejsce gdzie zachodzi 90 % prawdziwie efektywnej nauki. Będziemy Wam w tym pomagać poprzez udostępnianie każdego dnia planu powtórkowego. Naszym celem jest aby każde zagadnienie, które będzie zawarte przez Was w notatkach w Albumie Map Myśli zostało powtórzone minimum 3-krotnie.</p>
				<h4>Prezentacja</h4>
				<p>Na tym etapie poznajemy nowy materiał. Docelowo, każdą prezentację przerabiamy tylko raz. Istnieje oczywiście możliwość powrotu do wcześniejszych prezentacji w razie potrzeby. Jest to najbardziej wymagająca część lekcji ;)</p>
				<h4>Ćwiczenia</h4>
				<p>Pytania, zadania i przypadki klinicznych do rozwiązania w celu podniesienia efektywności nauki.</p>
				<h4>Ciekawostki</h4>
				<p>Tutaj, czekają na Was same niespodzianki ;) Wyjątkowa atmosfera, którą staramy się tworzyć na kursie “Więcej niż LEK” nie wynika jedynie z zastosowania nowoczesnych narzędzi komunikacyjnych oraz metodologii nauczania. Jesteśmy przekonani, że kurs może być niesamowitą okazją do pogłębiania swojej wiedzy nie tylko z zakresu medycyny. Chcemy się dzielić z Wami naszą pasją do uczenia się i tworzyć społeczność ludzi uczących. Propozycje książek, filmów czy TED-ów, a czasami jakiś “suchar” bądź historia. Wszystko to ma służyć szerzeniu idei szeroko pojętego “dzielenia się wiedzą”. :)</p>

				<h3>W jakim języku prowadzony jest kurs Więcej niż LEK?</h3>
				<p>Zarówno prezentacje multimedialne jak i Album Map Myśli są przygotowane w całości po polsku. Dodatkowo prezentacje będą czasami zawierać treści w języku angielskim. Będą to głównie linki do filmów edukacyjnych, dzięki którym będziemy mogli poszerzać nasze zrozumienie i ułatwić zapamiętanie treści. Podstawowa znajomość języka angielskiego pozwoli z nich w pełni korzystać jednak nie jest kluczowa w uczestnictwie w samym kursie, gdyż treści występujące tylko w języku angielskim nie przekroczą  1% całości. Poza tym, będzie to dodatkowa okazja do nauczenia się paru przydatnych słówek i angielskich mnemotechnik:)</p>

				<h3>Czy możemy uczyć się razem z Wami? Odwiedzić Was w siedzibie firmy i pomóc przy tworzeniu kursu?</h3>
				<p>W ramach projektu Więcej niż LEK w siedzibie firmy (ul.Henryka Sienkiewicza 8/1 na Poznańskich Jeżycach) od dnia 1 maja 2017 roku będzie udostępniona dla Was przestrzeń do nauki. Składa się na nią czytelnia, która może pomieścić komfortowo około 8 osób oraz biblioteka gdzie będą mogły uczyć się kolejne 3-4 osoby :) Jeśli macie ochotę pouczyć się u nas, poznać ekipę Więcej niż LEK osobiście, pomóc w tworzeniu kursu lub po prostu po ciężkim dniu nauki odetchnąć, poznać kilka osób i zagrać w planszówkę to zapraszamy Was serdecznie :)</p>

